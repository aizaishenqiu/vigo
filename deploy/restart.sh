#!/bin/bash
# ==============================================
# Vigo 应用重启脚本
# 适用于 CentOS 7.x 及以上版本
# ==============================================

# 获取脚本所在目录
APP_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$APP_DIR"

# 先停止应用
echo "正在停止应用..."
"$APP_DIR/stop.sh"

# 等待 2 秒
sleep 2

# 再启动应用
echo ""
echo "正在启动应用..."
"$APP_DIR/start.sh"
