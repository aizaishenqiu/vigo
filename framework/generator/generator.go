// generateModelFromSchema 根据实际表结构生成模型
func generateModelFromSchema(schema *TableSchema, output string) error {
	modelFile := filepath.Join(output, fmt.Sprintf("%s.go", toSnakeCase(schema.Name)))
	
	// 构建字段定义
	fieldDefinitions := make([]string, 0, len(schema.Columns))
	for _, column := range schema.Columns {
		fieldDef := buildFieldDefinition(column)
		fieldDefinitions = append(fieldDefinitions, fieldDef)
	}

	modelTmpl := `// Code generated by vigoctl. DO NOT EDIT.
// This file was generated at {{.Timestamp}}
// Table: {{.TableName}}

package model

import (
	"time"
)

// {{.ModelName}} {{.ModelDesc}}
type {{.ModelName}} struct {
{{.Fields}}
}

// TableName 表名
func (m *{{.ModelName}}) TableName() string {
	return "{{.TableName}}"
}
`

	tmpl, err := template.New("model").Parse(modelTmpl)
	if err != nil {
		return fmt.Errorf("解析模板失败：%w", err)
	}

	data := map[string]interface{}{
		"ModelName": toPascalCase(schema.Name),
		"ModelDesc": schema.Name + "模型",
		"TableName": schema.Name,
		"Timestamp": time.Now().Format("2006-01-02 15:04:05"),
		"Fields":    strings.Join(fieldDefinitions, "\n"),
	}

	file, err := os.Create(modelFile)
	if err != nil {
		return fmt.Errorf("创建文件失败：%w", err)
	}
	defer file.Close()

	if err := tmpl.Execute(file, data); err != nil {
		return fmt.Errorf("生成文件失败：%w", err)
	}

	fmt.Printf("✓ 模型生成成功 (基于实际表结构)：%s\n", modelFile)
	fmt.Printf("  包含 %d 个字段\n", len(schema.Columns))
	return nil
}

// buildFieldDefinition 构建字段定义
func buildFieldDefinition(column ColumnSchema) string {
	// Go 类型映射
	goType := mapSQLTypeToGo(column.Type)
	
	// JSON 标签
	jsonTag := fmt.Sprintf(`json:"%s"`, column.Name)
	
	// DB 标签
	dbTag := fmt.Sprintf(`db:"%s"`, column.Name)
	
	// 额外属性
	extraTags := ""
	if column.IsPrimary {
		extraTags += " pk"
	}
	if column.AutoIncrement {
		extraTags += " auto_increment"
	}
	
	// 注释
	comment := ""
	if column.Comment != "" {
		comment = " // " + column.Comment
	}
	
	return fmt.Sprintf("\t%s %s `%s %s%s`%s",
		toPascalCase(column.Name),
		goType,
		jsonTag,
		dbTag,
		extraTags,
		comment)
}

// mapSQLTypeToGo 将 SQL 类型映射到 Go 类型
func mapSQLTypeToGo(sqlType string) string {
	sqlType = strings.ToLower(sqlType)
	
	switch {
	case strings.Contains(sqlType, "int"):
		return "int64"
	case strings.Contains(sqlType, "decimal") || strings.Contains(sqlType, "numeric"):
		return "decimal.Decimal"
	case strings.Contains(sqlType, "float") || strings.Contains(sqlType, "double"):
		return "float64"
	case strings.Contains(sqlType, "bool"):
		return "bool"
	case strings.Contains(sqlType, "date") || strings.Contains(sqlType, "time"):
		if strings.Contains(sqlType, "time") {
			return "time.Time"
		}
		return "string"
	case strings.Contains(sqlType, "char") || strings.Contains(sqlType, "text"):
		return "string"
	case strings.Contains(sqlType, "json"):
		return "json.RawMessage"
	case strings.Contains(sqlType, "blob"):
		return "[]byte"
	default:
		return "string"
	}
}

