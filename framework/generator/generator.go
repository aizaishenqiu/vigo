// GenerateAPI 从 IDL 生成 API 代码
func GenerateAPI(input, output, style string) error {
	// 解析 IDL 文件
	apiDef, err := idl.Parse(input)
	if err != nil {
		return fmt.Errorf("解析 IDL 文件失败：%w", err)
	}

	// 创建输出目录
	if err := os.MkdirAll(output, 0755); err != nil {
		return fmt.Errorf("创建输出目录失败：%w", err)
	}

	// 根据风格生成不同的代码
	switch style {
	case "mvc":
		return generateMVC(apiDef, output)
	case "rpc":
		return generateRPC(apiDef, output)
	default:
		return fmt.Errorf("不支持的代码风格：%s", style)
	}
}

